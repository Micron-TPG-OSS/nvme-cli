ZSH_HEADER = '''\
#compdef _nvme nvme
# SPDX-License-Identifier: GPL-2.0-or-later
#
# zsh completions for the nvme command-line interface
#
# This file is generated by generate-completions.py. Do not edit.
'''

ZSH_HELPERS = '''\
\t# Describe options, giving those ending in '=' no trailing space.
\t_nvme_describe() {
\t\tlocal desc="$1"
\t\tlocal -a opts=("${(@P)2}")
\t\tlocal -a eq_opts noeq_opts
\t\tlocal opt

\t\tfor opt in "${opts[@]}"; do
\t\t\tif [[ "${opt%%:*}" == *= ]]; then
\t\t\t\teq_opts+=("$opt")
\t\t\telse
\t\t\t\tnoeq_opts+=("$opt")
\t\t\tfi
\t\tdone

\t\t(( ${#noeq_opts} )) && _describe -t options "$desc" noeq_opts
\t\t(( ${#eq_opts} )) && _describe -t eq-options "$desc" eq_opts -S ''
\t}

\t# Complete the value of an option (both --opt=val and --opt val forms).
\t# Usage: _nvme_complete_option_value "long|short" value1 value2 ...
\t_nvme_complete_option_value() {
\t\tlocal -a opts=("${(s:|:)1}")
\t\tshift
\t\tlocal -a values=("$@")

\t\tlocal opt
\t\tfor opt in "${opts[@]}"; do
\t\t\tif [[ ${words[CURRENT]} == ${opt}=* ]]; then
\t\t\t\t_values '' "${values[@]}"
\t\t\t\treturn 0
\t\t\tfi
\t\t\tif [[ ${words[CURRENT-1]} == ${opt} ]]; then
\t\t\t\t_values '' "${values[@]}"
\t\t\t\treturn 0
\t\t\tfi
\t\tdone
\t\treturn 1
\t}
'''


def zsh_q(s):
    """Escape a string for inclusion inside a zsh '...' single-quoted string."""
    return (s or "").replace("'", "'\\''")


def zsh_var(plugin, cmd):
    name = "_"
    if plugin:
        name += plugin.replace("-", "_") + "_"
    name += cmd.replace("-", "_")
    return name


def zsh_value_completions(out, tab, cmd):
    for o in cmd["options"]:
        vals = o.get("values")
        if not vals:
            continue
        sel = "--" + o["long"]
        if o.get("short"):
            sel += "|-" + o["short"]
        out.write(f'{tab}_nvme_complete_option_value "{sel}" {" ".join(vals)} && return\n')


def zsh_command_arm(out, tab, path, cmd):
    out.write(f"{tab}({cmd['name']})\n")
    if cmd.get("no_args") or is_meta(cmd):
        out.write(f"{tab}\t;;\n")
        return

    zsh_value_completions(out, tab, cmd)

    var = zsh_var(path, cmd["name"])
    out.write(f"{tab}local {var}\n{tab}{var}=(\n")
    for o in cmd["options"]:
        out.write(f"\t--{o['long']}{'=' if o.get('takes_value') else ''}':{zsh_q(o.get('help'))}'\n")
        if o.get("short"):
            out.write(f"\t-{o['short']}':alias for --{o['long']}'\n")
    out.write(f"{tab})\n")
    out.write(f"{tab}_arguments '*:: :->subcmds'\n")
    label = f"nvme {path + ' ' if path else ''}{cmd['name']} options"
    out.write(f'{tab}_nvme_describe "{label}" {var}\n')
    out.write(f"{tab}compadd -- /dev/nvme*(N)\n")
    out.write(f"{tab}\t;;\n")


def gen_zsh(model, out):
    out.write(ZSH_HEADER)
    out.write("\n_nvme () {\n\tlocal -a _cmds\n\t_cmds=(\n")
    for c in builtin_commands(model):
        out.write(f"\t'{c['name']}:{zsh_q(c.get('help'))}'\n")
    for p in plugins(model):
        out.write(f"\t'{p['name']}:{zsh_q(p.get('desc'))}'\n")
    out.write("\t)\n\n")

    out.write(ZSH_HELPERS)

    out.write("\n\tlocal expl\n\n"
              "\t_arguments '*:: :->subcmds' && return 0\n\n"
              "\tif (( CURRENT == 1 )); then\n"
              "\t\t_describe -t commands \"nvme subcommands\" _cmds\n"
              "\t\treturn\n"
              "\telif (( CURRENT > 2 )); then\n"
              "\t\tcase ${words[1]} in\n")

    for p in plugins(model):
        out.write(f"\t\t({p['name']})\n\t\t\tcase ${{words[2]}} in\n")
        for c in p["commands"]:
            zsh_command_arm(out, "\t\t\t", p["name"], c)
        out.write("\t\t\t(*)\n\t\t\t\t_files\n\t\t\t\t;;\n")
        out.write("\t\t\tesac\n\t\t\t;;\n")

    for c in builtin_commands(model):
        zsh_command_arm(out, "\t\t", None, c)

    out.write("\t\t(*)\n\t\t\t_files\n\t\t\t;;\n"
              "\t\tesac\n\t\treturn\n\tfi\n\n"
              "\t_files\n}\n")


