PS_HEADER = '''\
# SPDX-License-Identifier: GPL-2.0-or-later
#
# PowerShell argument completer for the nvme command line utility.
#
# This file is generated by generate-completions.py. Do not edit.
# Source it from your PowerShell profile to enable completion.
'''

PS_COMPLETER = '''
Register-ArgumentCompleter -Native -CommandName nvme -ScriptBlock {
    param($wordToComplete, $commandAst, $cursorPosition)

    $tokens = $commandAst.CommandElements | ForEach-Object { $_.ToString() }
    # tokens[0] is 'nvme'. Determine command context.
    $nonOpt = @($tokens[1..($tokens.Count-1)] | Where-Object { $_ -notlike '-*' })

    # Completing the (sub)command name itself.
    if ($nonOpt.Count -le 1) {
        return $script:NvmeCommands | Where-Object { $_ -like "$wordToComplete*" } |
            ForEach-Object { [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterValue', $_) }
    }

    $first = $nonOpt[0]
    $key = $null
    if ($script:NvmePluginCommands.ContainsKey($first)) {
        if ($nonOpt.Count -le 2) {
            return $script:NvmePluginCommands[$first] | Where-Object { $_ -like "$wordToComplete*" } |
                ForEach-Object { [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterValue', $_) }
        }
        $key = "$first $($nonOpt[1])"
    } else {
        $key = $first
    }

    if ($script:NvmeOptions.ContainsKey($key)) {
        return $script:NvmeOptions[$key] | Where-Object { $_ -like "$wordToComplete*" } |
            ForEach-Object { [System.Management.Automation.CompletionResult]::new($_, $_, 'ParameterName', $_) }
    }
}
'''


def ps_q(s):
    """Escape for a PowerShell single-quoted string (double the quote)."""
    return (s or "").replace("'", "''")


def ps_command_options(cmd):
    """The ('--opt', '-s', ...) token list for one command."""
    toks = []
    if not (cmd.get("no_args") or is_meta(cmd)):
        for o in cmd["options"]:
            toks.append("'--%s'" % o["long"])
            if o.get("short"):
                toks.append("'-%s'" % o["short"])
    return ", ".join(toks)


def gen_powershell(model, out):
    out.write(PS_HEADER)

    out.write("\n$script:NvmeCommands = @(\n")
    for c in builtin_commands(model):
        out.write(f"    '{c['name']}'\n")
    for p in plugins(model):
        out.write(f"    '{p['name']}'\n")
    out.write(")\n")

    out.write("\n$script:NvmePluginCommands = @{\n")
    for p in plugins(model):
        names = ", ".join("'%s'" % c["name"] for c in p["commands"])
        out.write(f"    '{p['name']}' = @({names})\n")
    out.write("}\n")

    out.write("\n$script:NvmeOptions = @{\n")
    for c in builtin_commands(model):
        out.write(f"    '{ps_q(c['name'])}' = @({ps_command_options(c)})\n")
    for p in plugins(model):
        for c in p["commands"]:
            key = ps_q("%s %s" % (p["name"], c["name"]))
            out.write(f"    '{key}' = @({ps_command_options(c)})\n")
    out.write("}\n")

    out.write(PS_COMPLETER)


# ---------------------------------------------------------------------------
# main
# ---------------------------------------------------------------------------

GENERATORS = {"bash": gen_bash, "zsh": gen_zsh, "powershell": gen_powershell}


def open_out(path):
    return sys.stdout if path == "-" else open(path, "w")


def main():
    ap = argparse.ArgumentParser(description=__doc__,
                                 formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("input", nargs="?", default="-",
                    help="JSON model file, or '-' for stdin (default)")
    ap.add_argument("--bash", metavar="FILE", help="write bash completion to FILE")
    ap.add_argument("--zsh", metavar="FILE", help="write zsh completion to FILE")
    ap.add_argument("--powershell", metavar="FILE",
                    help="write PowerShell completion to FILE")
    args = ap.parse_args()

    src = sys.stdin if args.input == "-" else open(args.input)
    model = json.load(src)

    requested = [(s, getattr(args, s)) for s in GENERATORS if getattr(args, s)]
    if not requested:
        ap.error("specify at least one of --bash/--zsh/--powershell")

    for shell, path in requested:
        out = open_out(path)
        try:
            GENERATORS[shell](model, out)
        finally:
            if out is not sys.stdout:
                out.close()


if __name__ == "__main__":
    main()
