#!/usr/bin/env python3
# SPDX-License-Identifier: GPL-2.0-or-later
"""Generate shell completion scripts for nvme-cli.

Input is the JSON metadata emitted by `nvme dump-commands-and-options`, read
from a file or stdin. The command/option model is captured at runtime from the
compiled binary, so this script does no C parsing -- it only formats.

Usage:
    nvme dump-commands-and-options | generate-completions.py --bash out.sh \\
        --zsh _nvme --powershell nvme-completion.ps1
    generate-completions.py model.json --bash -        # '-' writes to stdout
"""

import argparse
import json
import sys


# ---------------------------------------------------------------------------
# Model access helpers
# ---------------------------------------------------------------------------

def opt_token(opt):
    """The bash/option token for an option: '--name=' or '--name'."""
    return "--" + opt["long"] + ("=" if opt.get("takes_value") else "")


def is_meta(cmd):
    return bool(cmd.get("meta"))


def command_options(cmd, globals_only=False, locals_only=False):
    """Yield options, optionally filtered to globals or command-specific."""
    for opt in cmd["options"]:
        g = bool(opt.get("global"))
        if globals_only and not g:
            continue
        if locals_only and g:
            continue
        yield opt


def plugins(model):
    return model.get("plugins", [])


def builtin_commands(model):
    return model.get("commands", [])


# ---------------------------------------------------------------------------
# bash
# ---------------------------------------------------------------------------

BASH_HEADER = '''\
# SPDX-License-Identifier: GPL-2.0-or-later
#
# bash tab completion for the nvme command line utility
#
# This file is generated by generate-completions.py. Do not edit.

# Helper function to detect if we're completing an option's value.
# Uses: $cur, $prev, $words, $cword (from _init_completion)
# Sets: $opt (the option name), $val (partial value), $completing_value (0 or 1)
_nvme_detect_value_completion() {
\tcompleting_value=0
\topt=""
\tval=""

\tif [[ $cur == --*= ]]; then
\t\topt="${cur%=}"
\t\tcompleting_value=1
\telif [[ $cur == "=" ]] && [[ $prev == --* ]]; then
\t\topt="$prev"
\t\tcompleting_value=1
\telif [[ $cur != -* ]] && [[ $cur != "" ]] && [[ $prev == "=" ]] && [[ ${words[$cword-2]} == --* ]]; then
\t\topt="${words[$cword-2]}"
\t\tval="$cur"
\t\tcompleting_value=1
\telif [[ $cur != -* ]] && [[ $cur != "" ]] && [[ $prev == --* ]]; then
\t\topt="$prev"
\t\tval="$cur"
\t\tcompleting_value=1
\telif [[ $cur == "" ]] && [[ $prev == --* ]]; then
\t\topt="$prev"
\t\tcompleting_value=1
\telif [[ $cur == "" ]] && [[ $prev == "=" ]]; then
\t\topt="${words[$cword-2]}"
\t\tcompleting_value=1
\telif [[ $cur == "" ]] && [[ $prev == -? ]]; then
\t\topt="$prev"
\t\tcompleting_value=1
\telif [[ $cur != -* ]] && [[ $cur != "" ]] && [[ $prev == -? ]]; then
\t\topt="$prev"
\t\tval="$cur"
\t\tcompleting_value=1
\tfi
}
'''

BASH_FUNC_PREAMBLE = '''\

{func} () {{
\tlocal opts=""
\tlocal compargs=""
\tlocal vals=""
\tlocal opt=""
\tlocal val=""

\tlocal nonopt_args=0
\tlocal has_device=0
\tfor (( i=0; i < ${{#words[@]}}-1; i++ )); do
\t\tif [[ ${{words[i]}} != -* ]] && [[ ${{words[i]}} != "=" ]]; then
\t\t\tlet nonopt_args+=1
\t\t\tif [[ ${{words[i]}} == /dev/* ]]; then
\t\t\t\thas_device=1
\t\t\tfi
\t\tfi
\tdone

\tif [[ $nonopt_args -ge {device_argpos} ]] && [[ $has_device -eq 0 ]] && \\
\t   [[ "$1" != "help" ]] && [[ "$1" != "version" ]]; then
\t\topts="/dev/nvme* "
\tfi

\topts+=" "
\tvals+=" "

\tlocal completing_value=0
\t_nvme_detect_value_completion
'''

BASH_FUNC_EPILOGUE = '''\

\topts+=" -h --help"

\tif [[ $vals == " " ]]; then
\t\tCOMPREPLY+=( $( compgen $compargs -W "$opts" -- $cur ) )
\t\t[[ ${COMPREPLY-} == *= ]] && compopt -o nospace
\telse
\t\tCOMPREPLY+=( $( compgen $compargs -W "$vals" -- $val ) )
\tfi

\treturn 0
}
'''


def bash_option_tokens(opts):
    """Build the ' --name= -s ...' token string for a sequence of options."""
    out = ""
    for o in opts:
        out += " " + opt_token(o)
        if o.get("short"):
            out += " -" + o["short"]
    return out


def bash_value_arm(opt):
    """A 'case $opt in' arm for an option with a known value set, or ''."""
    vals = opt.get("values")
    if not vals:
        return ""
    sel = "--" + opt["long"]
    if opt.get("short"):
        sel += "|-" + opt["short"]
    return (f"\t\t\t\t{sel})\n"
            f"\t\t\t\tvals+=\" {' '.join(vals)}\"\n"
            f"\t\t\t\t\t;;\n")


def bash_plugin_func(out, commands, func, device_argpos):
    out.write(BASH_FUNC_PREAMBLE.format(func=func, device_argpos=device_argpos))

    # Shared arm: global options + value completion for non-meta commands.
    # Source the globals from the first command that has any.
    globals_src = next((c for c in commands if list(command_options(c, globals_only=True))), None)
    out.write('\tcase "$1" in\n\t\t"version"|"help")\n\t\t\t;;\n\t\t*)\n\t\topts+="')
    if globals_src:
        out.write(bash_option_tokens(command_options(globals_src, globals_only=True)))
    out.write('"\n')
    out.write('\t\tif [[ $completing_value -eq 1 ]]; then\n\t\t\tcase $opt in\n')
    if globals_src:
        for o in command_options(globals_src, globals_only=True):
            out.write(bash_value_arm(o))
    out.write('\t\t\tesac\n\t\tfi\n\t\t\t;;\n\tesac\n\n')

    # Per-command specific options.
    out.write('\tcase "$1" in\n')
    for c in commands:
        if is_meta(c):
            continue
        toks = bash_option_tokens(command_options(c, locals_only=True))
        out.write(f'\t\t"{c["name"]}")\n\t\topts+="{toks}"\n\t\t\t;;\n')
    out.write('\tesac\n')

    out.write(BASH_FUNC_EPILOGUE)


def bash_func_name(plugin):
    return "plugin_%s_opts" % plugin.replace("-", "_")


def gen_bash(model, out):
    out.write(BASH_HEADER)

    bash_plugin_func(out, builtin_commands(model), "nvme_list_opts", 2)
    for p in plugins(model):
        bash_plugin_func(out, p["commands"], bash_func_name(p["name"]), 3)

    # Dispatcher.
    out.write("\n_nvme_subcmds () {\n"
              "\tlocal cur prev words cword\n"
              "\t_init_completion || return\n"
              "\n"
              "\ttypeset -Ar _plugin_subcmds=(\n")
    for p in plugins(model):
        names = " ".join(c["name"] for c in p["commands"])
        out.write(f'\t\t[{p["name"]}]="{names}"\n')
    out.write("\t)\n\n\ttypeset -Ar _plugin_funcs=(\n")
    for p in plugins(model):
        out.write(f'\t\t[{p["name"]}]="{bash_func_name(p["name"])}"\n')
    out.write("\t)\n\n\t_cmds=\"")
    cmds = [c["name"] for c in builtin_commands(model)] + [p["name"] for p in plugins(model)]
    out.write(" ".join(cmds))
    out.write('"\n\n'
              "\tif [[ ${#words[*]} -lt 3 ]]; then\n"
              "\t\tCOMPREPLY+=( $(compgen -W \"$_cmds\" -- $cur ) )\n"
              "\telse\n"
              "\t\tfor subcmd in \"${!_plugin_subcmds[@]}\"; do\n"
              "\t\t\tif [[ ${words[1]} == $subcmd ]]; then\n"
              "\t\t\t\tif [[ ${#words[*]} -lt 4 ]]; then\n"
              "\t\t\t\t\tCOMPREPLY+=( $(compgen -W \"${_plugin_subcmds[$subcmd]}\" -- $cur ) )\n"
              "\t\t\t\telse\n"
              "\t\t\t\t\tfunc=${_plugin_funcs[$subcmd]}\n"
              "\t\t\t\t\t$func ${words[2]} $prev\n"
              "\t\t\t\tfi\n"
              "\t\t\t\treturn 0\n"
              "\t\t\tfi\n"
              "\t\tdone\n"
              "\n"
              "\t\tnvme_list_opts ${words[1]} $prev\n"
              "\tfi\n"
              "\n"
              "\treturn 0\n"
              "}\n"
              "\ncomplete -o default -F _nvme_subcmds nvme\n")


